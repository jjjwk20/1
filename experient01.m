% 题目：实现信号卷积 f(t) * h(t)
% 已知：f(t) = 2[ε(t) - ε(t-2)]，h(t) = e^(-t)ε(t)
clear; clc; close all;   % 清空工作区变量、清空命令行窗口、关闭所有已打开的图形窗口，避免旧数据干扰

%% ========== 1. 定义基础参数与时间轴 ==========
dt = 0.01;              % 时间步长，控制离散近似的精度
                        % 步长越小，离散信号越接近连续信号，卷积结果越准确
t = -1:dt:5;            % 定义时间向量，范围从-1到5
                        % 选-1是为了包含t<0的区域（信号为0，方便观察初始状态）
                        % 选5是因为h(t)=e^(-t)衰减很快，t>5时几乎为0，足够覆盖有效区间

%% ========== 2. 构造输入信号 f(t) = 2[ε(t) - ε(t-2)] ==========
% 说明：ε(t)是单位阶跃函数，MATLAB中用逻辑运算实现：
% (t >= 0) 当t≥0时返回1（真），t<0时返回0（假），等价于ε(t)
% (t >= 2) 等价于ε(t-2)
f = 2 * ((t >= 0) - (t >= 2));  
% 运算逻辑：
% - 当0 ≤ t < 2时：(t>=0)=1，(t>=2)=0 → 结果为2*(1-0)=2（矩形脉冲高电平）
% - 当t < 0或t ≥ 2时：结果为0（矩形脉冲低电平）

%% ========== 3. 构造系统冲激响应 h(t) = e^(-t)ε(t) ==========
% 说明：ε(t)保证t<0时h(t)=0，t≥0时h(t)=e^(-t)
h = exp(-t) .* (t >= 0);        
% .* 是逐元素乘法运算符：
% 对每个时间点t，先计算exp(-t)，再乘以阶跃结果(t>=0)，实现分段定义
% t<0时：(t>=0)=0 → h(t)=0；t≥0时：(t>=0)=1 → h(t)=exp(-t)

%% ========== 4. 计算卷积（离散近似连续卷积） ==========
y = conv(f, h) * dt;            
% 1. conv(f, h)：计算两个离散信号的卷积和，返回离散卷积结果
%    离散卷积的本质是：y[n] = Σf[k]·h[n-k]，和连续卷积的积分是"近似关系"
% 2. 乘以dt：将离散卷积和转换为连续卷积的近似值
%    连续卷积是积分运算：∫f(τ)h(t-τ)dτ，dt是积分步长，乘以dt后才是积分结果的近似
% 3. 结果长度：length(y) = length(f) + length(h) - 1，比原信号长

%% ========== 5. 生成卷积结果对应的时间轴 ==========
t_conv = (0:length(y)-1)*dt + (t(1) + t(1));  
% 说明：卷积结果的时间轴需要重新计算：
% - 两个信号的时间起点都是t(1)=-1，卷积后的时间起点为t(1)+t(1)=-2
% - 步长仍为dt，总点数为length(y)，因此时间轴从-2开始，步长dt，共length(y)个点

%% ========== 6. 绘图展示结果 ==========
figure('Color','w');            % 创建一个白色背景的图形窗口

% 子图1：绘制输入信号f(t)
subplot(3,1,1);                 % 创建3行1列的子图，当前选中第1个
plot(t, f, 'LineWidth',1.5);    % 绘制f(t)的曲线，线宽设为1.5，更清晰
grid on;                        % 显示网格线，方便读取坐标
xlabel('t (s)');                % X轴标签：时间（单位：秒）
ylabel('f(t)');                 % Y轴标签：信号幅度
title('输入信号 f(t) = 2[ε(t) - ε(t-2)]');  % 子图标题
ylim([-0.5 2.5]);               % 设置Y轴范围，让图像更美观（避免空白过多）

% 子图2：绘制冲激响应h(t)
subplot(3,1,2);                 % 选中第2个子图
plot(t, h, 'LineWidth',1.5);    % 绘制h(t)的曲线
grid on;
xlabel('t (s)');
ylabel('h(t)');
title('冲激响应 h(t) = e^{-t}ε(t)');
ylim([-0.1 1.1]);               % h(t)最大值为1，设置Y轴范围

% 子图3：绘制卷积结果y(t) = f(t)*h(t)
subplot(3,1,3);                 % 选中第3个子图
plot(t_conv, y, 'LineWidth',1.5, 'Color','r');  % 用红色绘制卷积结果，突出显示
grid on;
xlabel('t (s)');
ylabel('y(t) = f(t) * h(t)');
title('卷积结果 y(t)');
xlim([min(t) max(t_conv)]);     % 设置X轴范围，显示完整的卷积结果
