% 题目：三阶低通滤波器特性分析与实现
% 系统频率响应：H(ω) = 1/((jω)^3 + 3(jω)^2 + 2(jω) + 1)
clear; clc; close all;   % 清空工作区变量、清空命令行窗口、关闭所有已打开的图形窗口，避免旧数据干扰

%% ========== 1. 定义系统传递函数 ==========
% 传递函数的拉普拉斯形式：H(s) = 1/(s^3 + 3s^2 + 2s + 1)
% num: 分子多项式系数（按s降幂排列，分子为常数1，因此num=[1]）
num = [1];
% den: 分母多项式系数（按s降幂排列，s^3 + 3s^2 + 2s + 1，因此den=[1 3 2 1]）
den = [1 3 2 1];

%% ========== 2. 生成频率向量，计算频率响应 ==========
% 生成角频率向量ω（单位：rad/s），范围0到10rad/s，取1000个点保证曲线平滑
omega = linspace(0, 10, 1000);  

% 计算频率响应H(ω)：将s=jω代入传递函数，即H(jω) = num(jω)/den(jω)
% polyval(p, x) 计算多项式p在x处的值，这里p是num/den，x是jω
H = polyval(num, 1j*omega) ./ polyval(den, 1j*omega);  

%% ========== 3. 计算幅频特性和相频特性 ==========
% 幅频特性|H(ω)|：频率响应的模（描述幅度增益）
mag = abs(H);  
% 相频特性φ(ω)：频率响应的相位角，单位转换为度（angle返回弧度，乘以180/pi）
phase = angle(H) * 180/pi;  

%% ========== 4. 绘制幅频特性和相频特性 ==========
figure('Color','w');  % 创建白色背景的图形窗口

% 子图1：幅频特性曲线
subplot(2,1,1);  
plot(omega, mag, 'LineWidth',1.5);  % 绘制幅频曲线，线宽设为1.5让曲线更清晰
grid on;  % 显示网格线，方便读取坐标
xlabel('\omega (rad/s)');  % X轴标签：角频率（单位rad/s）
ylabel('|H(\omega)|');     % Y轴标签：幅频特性
title('三阶低通滤波器幅频特性');  % 子图标题
ylim([0 1.1]);  % 设置Y轴范围（0到1.1），避免曲线被截断或空白过多

% 子图2：相频特性曲线
subplot(2,1,2);  
plot(omega, phase, 'LineWidth',1.5, 'Color','r');  % 红色绘制相频曲线，突出显示
grid on;
xlabel('\omega (rad/s)');
ylabel('\phi(\omega) (degrees)');  % Y轴标签：相频特性（单位：度）
title('三阶低通滤波器相频特性');
ylim([-270 0]);  % 设置Y轴范围（-270到0），三阶系统最大相移为-270°

%% ========== 5. 计算并绘制冲激响应h(t) ==========
% 创建传递函数模型（Control System Toolbox中的tf函数）
sys = tf(num, den);  

figure('Color','w');  % 创建新的图形窗口，绘制冲激响应
impulse(sys);  % 绘制系统的冲激响应曲线（h(t)）
grid on;
title('三阶低通滤波器冲激响应h(t)');
xlabel('t (s)');  % X轴标签：时间（单位：秒）
ylabel('h(t)');   % Y轴标签：冲激响应幅度
